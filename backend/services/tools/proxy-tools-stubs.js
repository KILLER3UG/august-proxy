/**
 * Generate a proxy_tools.py module inside *outputDir*.
 *
 * @param {string} outputDir  Directory where proxy_tools.py will be created.
 * @returns {string}          Full path to the generated proxy_tools.py.
 */
function generateProxyToolsPy(outputDir) {
  const target = path.resolve(outputDir, 'proxy_tools.py');
  fs.mkdirSync(outputDir, { recursive: true });
  fs.writeFileSync(target, PROXY_TOOLS_PY_SOURCE, 'utf-8');
  return target;
}

module.exports = {
  generateProxyToolsPy,
  PROXY_TOOLS_PY_SOURCE
};
